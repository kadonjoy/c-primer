#include <iostream>
using namespace std;
int main(int argc, const char *argv[]) {
    cout << "dec: " << dec << 12 << endl;
    cout << "octal: 0" << oct << 12 << endl;
    cout << "hex: 0x" << hex << 12 << endl;
    cout << "a floating-number: " << 3.1415 << endl;
    cout << "ACSII code: " << char(27) << endl;

    return 0;
}


